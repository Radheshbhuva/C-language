	pf("Enter the Value for Second Angle of Triangle : ");
	sf("%f", &angle2);
pf("\n");
	angle3 = (angle1 + angle2) - A;

pf("\n\n");
	pf("The Value of the angle3 is : %.2f degrees\n", angle3);

